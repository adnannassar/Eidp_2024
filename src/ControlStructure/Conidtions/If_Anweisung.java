package ControlStructure.Conidtions;

public class If_Anweisung {
    public static void main(String[] args) {
        int x = 1;
        if (x > 0) {
            System.out.println("X ist Positive!");
        } else if (x < 0) {
            System.out.println("X ist Negative!");
        }
        else {
            System.out.println("X ist 0");
        }
    }
}
