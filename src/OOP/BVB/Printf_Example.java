package OOP.BVB;

public class Printf_Example {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        System.out.println("X = " + x + ", Y = " + y);
        System.out.printf("X = %-10d, Y = %d\n",x , y);

        double  z = 11.123412356;
        System.out.printf("\n%.2f" , z);


    }
}
