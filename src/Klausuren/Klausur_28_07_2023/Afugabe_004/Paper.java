package Klausuren.Klausur_28_07_2023.Afugabe_004;

public class Paper {
    private int paperId;
    private String title;

    public Paper(int paperId, String title) {
        this.paperId = paperId;
        this.title = title;

    }

    public int getPaperId() {
        return paperId;
    }

    public String getTitle() {
        return title;
    }
}
